    <h1>Règlement du <?php echo $tournoi ?>, année <?php echo $annee ?></h1>

    <h2>Les équipes</h2>
    <p>Les équipes sont composées de 2 à 3 joueurs. Les matchs opposeront deux équipes avec le <b>même nombre de joueurs</b>, soit en triplette (3 joueurs), soit en doublette (2 joueurs) ou en tête-à-tête (un seul joueur).</p>

    <p>En triplette, chaque joueur dispose de 2 boules. En doublette et en tête-à-tête, chaque joueur dispose de 3 boules.</p>

    <h2>Aires de jeu et parties</h2>
    <p>Les parties se dérouleront sur tout terrain utilisable sur le <? echo $lieu ?>. Celui-ci devra être de largeur et longueur suffisante (l'idéal est 4m x 15m).</p>

    <p>Une partie se déroule en 13 points.</p>

    <h2>Le jeu</h2>
    <p>Les joueurs tirent au sort pour déterminer quelle équipe lancera la première le but (appelé aussi « bouchon »).</p>

    <p>Le point de départ pour le lancer du but est choisi librement. Le lanceur trace un cercle (environ 40 cm de diamètre). Lors des lancers (boules et buts), les pieds doivent rester à l'intérieur du cercle.</p>

    <p>Le but lancé doit être au moins à 6m et au plus à 10m du cercle, être à plus de 1m de tout obstacle et être visible.</p>

    <p>Le but sera annulé s'il est déplacé hors des limites de terrain pendant le jeu. La mène est nulle (aucun point acquis).</p>

    <p>Il est interdit de modifier l'espace de jeu (enlever ou déplacer des obstacles).</p>

    <p>La première boule d'une mène est lancée par un joueur de l'équipe ayant gagné le point précédent (ou le tirage au sort initial). Par la suite, c'est à l'équipe qui n'a pas le point de jouer. Toute boule lancée ne peut être rejouée.</p>

    <p>Pendant le lancer d'une boule, les autres joueurs ou spectateurs doivent être silencieux et immobiles.</p>

    <p>Une boule en dehors du terrain est annulée et retirée du jeu.</p>

    <h2>Mesures et points</h2>

    <p>La mesure d'un point incombe au joueur qui a joué en dernier. Les boules enlevées avant la mesure sont nulles.</p>

    <p>Lorsqu'il n'est pas possible de départager les boules qui « <i>prennent</i> » (elles sont à égale distance), alors :
    <ul>
        <li>si les deux équipes n'ont plus de boules, la mène est nulle et le but reste à l'équipe qui l'avait.</li>
        <li> s'il reste des boules, la partie continue pour l'équipe qui a joué en dernier (elle n'a pas réussi à faire mieux).</li>
    </ul>
    Les seuls points comptabilisés sont ceux donnés par des boules mieux placées que celles en égalité.</p>

<p>La première équipe à 13 points gagne la partie.</p>

<h2>Organisation des matchs</h2>

<p>Le tournoi est organisé par élimination en plusieurs tours, avec consolante pour les vaincus du premier tour.</p>

<p>Les matchs d'un tour doivent être effectués avant les dates indiquées.</p>

<p> Les équipes doivent convenir d'un rendez-vous et du nombre de joueurs qui seront présents. L'absence d'une équipe au rendez-vous fixé conduira à son forfait.</p>

<p>Tout match non effectué avant la date indiquée conduira au forfait d'une équipe par tirage au sort.</p>

<p> La finale et la consolante seront organisées lors de la journée de la recherche.</p>

